import numpy as np

"""Array bidimensional, 2 linhas e
4 colunas, criado e inicializado a zeros"""
print('Array bidimensional inicializado a zeros')
n_zeros = np.zeros(shape=(2, 4), dtype=int)
print(n_zeros)
"""Array unidimensional, 3 elementos, criado e
inicializado a zeros"""
print('Array unidimensional, criado e inicializado a zeros')
n_zeros = np.zeros(shape=(3))
print(n_zeros)
